package Week3.Roles;

public class Rating extends Qualification {

	private int score;
	private string region;

	public int getScore() {
		return this.score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public string getRegion() {
		return this.region;
	}

	public void setRegion(string region) {
		this.region = region;
	}
}